package kr.co.onpe.vo;

public class Push_List_VO {
	private String push_number;
	private String push_title;
	private String push_content;
	private String push_reservation_time;
	private String push_create_date;
	private String push_state;
	
	public String getPush_number() {
		return push_number;
	}
	public void setPush_number(String push_number) {
		this.push_number = push_number;
	}
	public String getPush_title() {
		return push_title;
	}
	public void setPush_title(String push_title) {
		this.push_title = push_title;
	}
	public String getPush_content() {
		return push_content;
	}
	public void setPush_content(String push_content) {
		this.push_content = push_content;
	}
	public String getPush_reservation_time() {
		return push_reservation_time;
	}
	public void setPush_reservation_time(String push_reservation_time) {
		this.push_reservation_time = push_reservation_time;
	}
	public String getPush_create_date() {
		return push_create_date;
	}
	public void setPush_create_date(String push_create_date) {
		this.push_create_date = push_create_date;
	}
	public String getPush_state() {
		return push_state;
	}
	public void setPush_state(String push_state) {
		this.push_state = push_state;
	}
	
}
