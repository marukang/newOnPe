package kr.co.onpe.vo;

/* 새소식 오브젝트 */
public class Notice_List_VO {
	
	private String notice_number;
	private String notice_class_code;
	private String notice_title;
	private String notice_content;
	private String notice_type;
	private String notice_date;
	private String notice_id;
	private String notice_name;
	
	public String getNotice_number() {
		return notice_number;
	}
	public void setNotice_number(String notice_number) {
		this.notice_number = notice_number;
	}
	public String getNotice_class_code() {
		return notice_class_code;
	}
	public void setNotice_class_code(String notice_class_code) {
		this.notice_class_code = notice_class_code;
	}
	public String getNotice_title() {
		return notice_title;
	}
	public void setNotice_title(String notice_title) {
		this.notice_title = notice_title;
	}
	public String getNotice_content() {
		return notice_content;
	}
	public void setNotice_content(String notice_content) {
		this.notice_content = notice_content;
	}
	public String getNotice_type() {
		return notice_type;
	}
	public void setNotice_type(String notice_type) {
		this.notice_type = notice_type;
	}
	public String getNotice_date() {
		return notice_date;
	}
	public void setNotice_date(String notice_date) {
		this.notice_date = notice_date;
	}
	public String getNotice_id() {
		return notice_id;
	}
	public void setNotice_id(String notice_id) {
		this.notice_id = notice_id;
	}
	public String getNotice_name() {
		return notice_name;
	}
	public void setNotice_name(String notice_name) {
		this.notice_name = notice_name;
	}
	
	
}
