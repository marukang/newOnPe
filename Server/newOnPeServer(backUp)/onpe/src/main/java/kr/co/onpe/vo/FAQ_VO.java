package kr.co.onpe.vo;

/* FAQ 오브젝트 */
public class FAQ_VO {
	
	private String faq_number;
	private String faq_target;
	private String faq_title;
	private String faq_content;
	private String faq_date;
	private String faq_type;
	public String getFaq_number() {
		return faq_number;
	}
	public void setFaq_number(String faq_number) {
		this.faq_number = faq_number;
	}
	public String getFaq_target() {
		return faq_target;
	}
	public void setFaq_target(String faq_target) {
		this.faq_target = faq_target;
	}
	public String getFaq_title() {
		return faq_title;
	}
	public void setFaq_title(String faq_title) {
		this.faq_title = faq_title;
	}
	public String getFaq_content() {
		return faq_content;
	}
	public void setFaq_content(String faq_content) {
		this.faq_content = faq_content;
	}
	public String getFaq_date() {
		return faq_date;
	}
	public void setFaq_date(String faq_date) {
		this.faq_date = faq_date;
	}
	public String getFaq_type() {
		return faq_type;
	}
	public void setFaq_type(String faq_type) {
		this.faq_type = faq_type;
	}
	
	
}
